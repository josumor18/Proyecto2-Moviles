package moviles.apps.proyecto2.friendtec.Business;

public class Usuario_Singleton {
    private static final Usuario_Singleton ourInstance = new Usuario_Singleton();

    private String id;
    private String nombre;
    private String carnet;
    private String email;
    private String foto;
    private String auth_token;

    public static Usuario_Singleton getInstance() {
        return ourInstance;
    }

    private Usuario_Singleton() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCarnet() {
        return carnet;
    }

    public void setCarnet(String carnet) {
        this.carnet = carnet;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getAuth_token() {
        return auth_token;
    }

    public void setAuth_token(String auth_token) {
        this.auth_token = auth_token;
    }
}
